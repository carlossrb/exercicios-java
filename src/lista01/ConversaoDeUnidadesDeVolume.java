package lista01;

public class ConversaoDeUnidadesDeVolume {
    public static double litroParaCm3(double value) {
        return value * 1000;
    }

    public static double metro3ParaLitro(double value){
        return value * 1000;
    }

    public static double metro3ParaCm3(double value) {
        return value * 1000;
    }

    public static double metro3ParaPe3(double value) {
        return value * 35.32;
    }

    public static double galaoParaPolegada3(double value) {
        return value * 231;
    }

    public static double galaoParaLitros(double value) {
        return value * 3785;
    }

}
